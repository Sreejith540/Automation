package control_statements;

public class Else_if {

	public static void main(String[] args) {
		
		int a=43, b=100, c=189;
		
		if (a>b && a>c)
		{
			System.out.println("a is greater");
		}
		else if (b>a && b>c)
		{
			System.out.println("b is greater");
		}
		else 
		{
			System.out.println("c is greater");
		}
		
		
		
			
	
	}

}
